import csv
'''
以下のようなCSVファイルを作成する
題名|執筆者|更新日|本文
おはよう|伊藤博文|1885/12/22|初代だよ
こんにちは|黒田清隆|1888/4/30|2代目だよ
こんばんは|山縣有朋|1889/12/24|カワウソみたいな顔
'''
row0 = ["題名","執筆者","更新日","本文"]
row1 = ["おはよう","伊藤博文","1885/12/22","初代だよ"]
row2 = ["こんにちは","黒田清隆","1888/4/30","2代目だよ"]
row3 = ["こんばんは","山縣有朋","1889/12/24","カワウソみたいだよ"]
rows = [row0, row1, row2, row3]

with open("data2.csv",  'w',encoding="shift-jis") as f:
    writer = csv.writer(f)
    writer.writerows(rows)
